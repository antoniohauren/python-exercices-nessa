numero1 = float(input("Digite o 1o numero: "))
operacao = input("Operacao: ")
numero2 = float(input("Digite o 2o numero: "))


if operacao == "+":
    # resultado = numero1 + numero2
    print(numero1 + numero2)
elif operacao == "-":
    # resultado = numero1 - numero2
    print(numero1 - numero2)
elif operacao == "*":
    # resultado = numero1 * numero2
    print(numero1 * numero2)
elif operacao == "/":
    # resultado = numero1 / numero2
    print(numero1 / numero2)

